    private void jButtonCadastrar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCadastrar2MouseClicked
        // TODO add your handling code here:
        Connection con;
        try {
            con = Conexao.getConnection();
            Statement statement = con.createStatement();
            if("".equals(textNome1.getText())  || "".equals(textEndereco1.getText())){
                JOptionPane.showMessageDialog(
                null, "Preecha os campos: \n Nome e Endere√ßo", "Aviso",
                JOptionPane.PLAIN_MESSAGE );
            }
            if("".equals(textTelefone1.getText().replaceAll("[( ) -]","")) && "".equals(textCelular1.getText().replaceAll("[( ) -]","")) ){
                JOptionPane.showMessageDialog(
                null, "Preecha pelo menos um n√∫mero para contato", "Aviso",
                JOptionPane.PLAIN_MESSAGE );
            }else{
            Cliente cl=new Cliente();
            String telefone = textTelefone1.getText();
            //telefone = telefone.replaceAll("[()-]","");// retirando os caracterres (   )  - da mascara
            //int tel=JSType.toInt32(telefone);   // convertendo para inteiro
            cl.setTelefone(telefone);      
            String celular =textCelular1.getText(); //celular=celular.replaceAll("[()-]","");
            //int cel=JSType.toInt32(celular);   // convertendo para inteiro
            cl.setCelular(celular);
            
            String query = "INSERT INTO cliente(" +
               "nome, endereco, telefone, celular,cpf)VALUES(' " +
               textNome1.getText() + "', '" + 
               textEndereco1.getText() + "'," + cl.getTelefone() + "," + cl.getCelular() +")";
                  
           int result = statement.executeUpdate(query);
                if ( result == 1 ){
                  JOptionPane.showMessageDialog(
                  null, "A inser√ß√£o foi um sucesso.", "Aviso",
                  JOptionPane.PLAIN_MESSAGE );
                  // fechar a tela 
                  this.dispose();
                }else {
                  JOptionPane.showMessageDialog(
                  null, "A inser√ß√£o falhou!. Verifique os campos", "Aviso",
                  JOptionPane.WARNING_MESSAGE );    
                }
          }
        }catch(SQLException ex){
            // a linha a baixo exibe a mensagem de falha ao conectar no banco
            JOptionPane.showMessageDialog(
                  null, "Falha ao conectar banco de dados", "Aviso",
                  JOptionPane.PLAIN_MESSAGE );
        }
    }//GEN-LAST:event_jButtonCadastrar2MouseClicked

